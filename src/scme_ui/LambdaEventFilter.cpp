#include "LambdaEventFilter.h"


///////////////////////////////////////////////////////////////////////////

using namespace ::SCME;


//////////////////////////////////////////////////////////////////////////

LambdaEventFilter::LambdaEventFilter(FilterFunc func, QObject* parent) :
    QObject(parent), func(std::move(func))
{
}

//////////////////////////////////////////////////////////////////////////

bool LambdaEventFilter::eventFilter(QObject* obj, QEvent* event)
{
    return func(obj, event);
}
